const { exec } = require('child_process');


const assert = (expr, msg) => {
    if (!expr) {
        console.log("Failed assertion check");
        console.error(msg);
        console.log("Dumping state:");

        console.log(`Cursor: ${cursor}`);
        console.log(JSON.stringify(allProblems));
        process.exit(-1);
    }
};

let cursor = 0;
let allProblems = {};

function fetchUri(uri) {
    return new Promise((resolve) => {
        const potwUri = uri,
            fetchCmd = `curl -L --silent ${potwUri} | egrep -oi 'data-solvable="([0-9]+)"'`,
            potwMatch = potwUri.match(/weekly-problems\/(\d+\-\d+\-\d+)\/(basic|intermediate|advanced)/),
            potwDate = potwMatch[1],
            potwLevel = potwMatch[2];

        exec(fetchCmd, (err, stdout, stderr) => {
            if (err) {
                assert(false, `err: ${err}`);
            } else if (stderr) {
                assert(false, `STDERR: ${stderr}`);
            } else {
                let problems = [];
                stdout.split('\n').forEach((data) => {
                    if (data === "") return;

                    const matchList = data.match(/"(\d+)"$/);
                    assert(matchList && matchList.length > 0, `Unexpected matchList on data: ${matchList} from "${data}"`);
                    const id = matchList[1];
                    if (problems.indexOf(id) === -1) problems.push(id);
                });

                if (!allProblems[potwDate]) allProblems[potwDate] = {};
                assert(!allProblems[potwDate][potwLevel], `Duplicate fetch for ${potwDate} ${potwLevel}`);
                allProblems[potwDate][potwLevel] = problems;
                console.log(`Fetched ${potwDate} ${potwLevel}`);
                resolve();
            }
        });
    });
};


function fetchAllUris() {
    fetchUri(uriList[cursor]).then(() => {
        ++cursor;
        if (cursor >= uriList.length) {
            console.log("Finished!");
            console.log(JSON.stringify(allProblems));
        } else {
            setTimeout(fetchAllUris, 500);
        }
    });
};

